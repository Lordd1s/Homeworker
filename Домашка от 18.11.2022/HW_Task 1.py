print("Программа которое сложит и умножает числы!")
num_1 = float(input("Введите 1 число: "))
num_2 = float(input('Введите 2 число: '))
num_3 = float(input('Введите 3 число: '))
plus = num_1 + num_2 + num_3
multiple = num_1 * num_2 * num_3
print("Сложение всех чисел: ", plus)
print("Умножение всех чисел: ", multiple)
